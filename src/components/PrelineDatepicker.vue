<template>
  <input
    ref="datepicker"
    class="hs-datepicker py-3 px-4 block w-full border-gray-200 rounded-lg sm:text-sm focus:border-blue-600 focus:ring-blue-600 disabled:opacity-50 disabled:pointer-events-none dark:bg-neutral-900 dark:border-neutral-700 dark:text-neutral-400 dark:placeholder:text-neutral-400 dark:focus:border-blue-500 dark:focus:ring-neutral-500"
    type="text"
    placeholder="Select day"
    readonly
    :data-hs-datepicker="config"
  >
</template>

<script setup>
import { onMounted, ref } from 'vue'
import { Datepicker as VCalendar } from 'vanillajs-datepicker'

const datepicker = ref(null)

const config = JSON.stringify({
  type: "default",
  dateMax: "2050-12-31",
  mode: "custom-select",
  templates: {
    arrowPrev:
      '<button data-vc-arrow="prev"><svg class="shrink-0 size-4" xmlns="http://www.w3.org/2000/svg" width="24" height="24" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"><path d="m15 18-6-6 6-6"></path></svg></button>',
    arrowNext:
      '<button data-vc-arrow="next"><svg class="shrink-0 size-4" xmlns="http://www.w3.org/2000/svg" width="24" height="24" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"><path d="m9 18 6-6-6-6"></path></svg></button>',
  }
})

onMounted(() => {
  if (window.hs?.Datepicker) {
    window.hs.Datepicker.autoInit()
  } else {
    // Fallback if Preline doesn't load
    new VCalendar(datepicker.value)
  }
})
</script>
